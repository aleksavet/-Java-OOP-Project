package Country;


/**

 The {@code HeavyFoodCountry} class represents a country that is known for its heavy and rich cuisine.

 It is a subclass of the {@link Country} class and inherits all of its properties.



 @see Country
 */

public class HeavyFoodCountry extends Country{
    public HeavyFoodCountry (String climate)
    {
        super(climate,"Heavy");
    }
}
